  <strong>What is tax at source?</strong>
  <p>
    In a system of tax at source (also known as pay-as-you-earn
    (UK) or withholding tax (USA)), the tax due is deducted by the
    employer directly from the employee’s salary and passed on to
    the state (in this case the Canton of Bern Tax Administration).
  </p>
    <strong>Who pays tax at source?</strong>
    <ul>
      <li>Foreign nationals working in Switzerland who do not hold a settlement permit (C permit).</li>
      <li>Employees working in Switzerland who live abroad, regardless of their nationality (for example international weekly residents, short-term residents, lorry drivers, cross-border commuters). </li>
      <li>Other persons who live abroad but who have to pay tax in Switzerland (artists, speakers, sportsmen and women, managers of legal entities, former employees with employeehares, mortgage creditors, recipients of benefits from Swiss pension schemes).</li>
    </ul>

  <strong>What is a “mandatory retrospective ordinary assessment”?</strong>
  <p>
    Under the law, if you are resident in Switzerland and liable to pay
    tax at source, a retrospective ordinary assessment will
    nonetheless be made if you:
    <ul>
      <li>are paid a gross salary of CHF 120,000 or more as an employee in any tax year,</li>
      <li>have taxable assets of CHF 150,000 or more at the end of the tax year or tax period or</li>
      <li>receive additional income of CHF 3,000 or more in any tax year that is not subject to tax at source (such as income from self-employed work, maintenance, an orphan’s pension, widow or widower’s pension or income from investments or real estate).</li>
    </ul>
  <strong>When do I become eligible for ordinary assessment?</strong>
  <p>
    If you are resident in Switzerland, your income and assets are
    subject to the ordinary assessment system from the start of
    the tax year in which the requirements for taxation at source
    cease to apply. However, tax will continue to be deducted at
    source until the end of the month in which the requirements for
    taxation at source cease to apply. Any taxes already deducted at
    source will be taken into account in the subsequent assessment.
    You become eligible for ordinary assessment:
    <ul>
      <li>when you or your spouse (provided you are not separated)
    acquires a settlement permit (C permit) or Swiss citizenship,</li>
      <li>when you marry a person with Swiss citizenship or
    who holds a C permit,</li>
      <li>if you are in receipt of a full invalidity pension,</li>
      <li>when you reach the age of retirement, provided you no
    longer receive income subject to tax at source.</li>
  </ul>
